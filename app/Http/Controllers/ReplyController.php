     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rpl = 'reply'.$request->id_message;
        $cntrpl = $request->$rpl;

        if ($validated = $request->validate([
            'id_message' => 'required',
            $rpl => 'required|max:150'
        ])) {
            Reply::create([
                'id_message' => $request->id_message,
                'reply' => $cntrpl
            ]);
            return back()->with('rplscss','Reply sent successfully');  
        }else{
            return back()->with('rplfail','Reply failed to send');
        }
        

    }

